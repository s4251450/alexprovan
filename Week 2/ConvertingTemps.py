class ConvertingTemps:
    @staticmethod
    def main():

        #declaring variables
        fahrenheit = 67.5
        celsius = (5/9)*(fahrenheit - 32)

        #printing variables
        print("Degrees in Fahrenheit = " + str(fahrenheit) + "F")
        print("Degrees in Celsius = " + str(celsius) + "C")


#initiating main method
ConvertingTemps.main()